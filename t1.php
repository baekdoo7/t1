3월 한 달간 대대적인 창립 행사를 열고 있는 홈플러스가 이번에는 파격적인 고기 할인전을 마련했다.<br>…<br>홈플러스는 7일부터 13일까지 일주일간 ‘쇼핑하라 2019’ 제2탄으로 육해공 고기를 모아 싸게 파는 ‘고기 대방출’ 기획전을 실시한다고 밝혔다.</p><p><br>홈플러스는 이번 행사를 위해 국내산 돈육은 평소의 3배 물량인 101톤을, 수입 찜갈비는 평소의 15배인 30톤, 생닭은 15배인 7만 마리, 고등어는 10배인 5만 마리를 준비하는 등 대규모 물량을 사전 기획해 가격을 획기적으로 낮췄다.<br><br>우선 매주 특정 기간 동안 파격가에 선보이는 ‘쇼킹 특가’ 상품으로 7~9일 3일간 초이스등급 찜갈비(미국산, 100g)를 1190원에 마련했으며, 7~8일 이틀간 두마리 생닭(500g*2마리)을 마리당 3990원에, 7일 하루는 부산 생물 고등어(1마리)를 990원에 판다.<br><br>또 일주일간 초이스등급 부채살(미국산, 100g)은 1590원, 척아이롤(호주산, 100g) 1390원, 우삼겹 양념 양지구이(호주산, 1kg) 1만3990원, 후라이드 청춘윙봉(20입)은 개당 450원 수준인 8990원에 판다.<br><br>브랜드 돈육 앞다리, 갈비, 뒷다리 부위는 마이홈플러스 회원 대상 30%  할인해 주며, 마포식 양념목심구이, 마포식 돼지수제포갈비, 마포식 양념갈매기살, 홍천식 고추장삼겹살구이,  언양식 석쇠구이 등 브랜드 양념육 5종은 2팩 이상 구매 시 10% 할인 판매한다.&nbsp;<br> <br>참치뱃살 모둠회 1만9900원, 굴비 전품목 11종(마이홈플러스회원 할인) 30% 할인, 에콰도르 흰다리새우(40마리 내외)는 1만2900원에 마련했다.<br><br>고기와 함께 곁들이기에 좋은 다양한 식품도 저렴하게 내놨다. 와인 180여 종은 2개 구매 시 20% 할인해 주며, 세계맥주 7종 4캔 8500원, 완도 통전복(팩) 1만9900원, 가리비(1.6kg 내외) 9990원, 통육포(100g+100g)는 9990원에 판매한다. </p><p><br></p><p>이밖에 딸기(1.6kg) 9990원, 대추·방울토마토(1.2kg) 6990원, 아보카도(5~9입) 9990원, 대용량 씨없는 청포도(1.8kg) 9990원, 실속사과(3.2kg, 신한, 삼성, 마이홈플러스 카드 할인) 9990원, 다다기오이(7입) 3990원, 양상추 및 파프리카 각 990원, CJ  햇반 실속(210g*12입)은 9980원이다. 8~9일 이틀간은  결제 금액 포인트도 기존 2배로 적립해 준다.<br><br>이창수 홈플러스 마케팅총괄부장은 “창립 22주년을 기념하고 서민 물가 안정에 기여하고자 3~6개월 전부터 품목별로 많게는 100배 이상의 물량을 확보하고 자체 마진 축소를 통해 주요 생필품 가격을 낮췄다”며 “3월 한 달간 대규모 행사를 전개하는 한편, 앞으로도 고객들의 생활 속에서 빼놓을 수 없는 상품들을 중심으로 물가 안정에 앞장서 나갈 것”이라고 말했다. <br></p>					</div>
